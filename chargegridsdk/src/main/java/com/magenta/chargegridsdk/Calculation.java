package com.magenta.chargegridsdk;

public class Calculation {

    public static  int add(int a, int b){
        return a+b;
    }

    public static  int minus(int a, int b){
        return a-b;
    }

    public static  int div(int a, int b){
        return a/b;
    }
    public static  int mul(int a, int b){
        return a*b;
    }

}
